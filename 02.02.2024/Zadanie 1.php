Metoda GET
Wady:
- Można ujawnić dane prywatne użytkownika w adresie URL.
Zalety:
- Nie potrzeba formularza aby zmodyfikować zawartość tych zmiennych.
- Łatwo dodać taką stronę do "ulubionych" ponieważ zakładka będzie kierować wprost do strony z wynikami wyszukiwania.

Metoda POST
Wady:
- Nie można łączyć się z parametrami.
- Nie można indeksować.
Zalety:
- Poufnie przekazuje dane.
- Dobrze się nadaje do rozbudowanych formularzy.